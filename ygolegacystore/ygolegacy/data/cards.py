from sqlalchemy import Column, INTEGER, VARCHAR, FLOAT, VARCHAR, DateTime, func, ForeignKey
from sqlalchemy.dialects.mysql import LONGTEXT
from sqlalchemy.orm import relationship

from ygolegacy.data.modelbase import SqlAlchemyBase


class CardPriceHistory(SqlAlchemyBase):
    __tablename__ = 'cards_price_history'
    id = Column(INTEGER, primary_key=True, autoincrement=True, index=True)
    card_id = Column(INTEGER, ForeignKey('cards.id'), index=True)
    site = Column(VARCHAR(255), index=True)
    price_cad = Column(FLOAT, index=True)
    price_usd = Column(FLOAT, index=True)
    average_low_cad = Column(FLOAT, index=True)
    average_high_cad = Column(FLOAT, index=True)
    average_low_usd = Column(FLOAT, index=True)
    average_high_usd = Column(FLOAT, index=True)
    time = Column(DateTime(timezone=True), onupdate=func.now(), index=True)
    parent_card = relationship("Card", back_populates='history')


class Card(SqlAlchemyBase):
    __tablename__ = 'cards'
    id = Column(INTEGER, primary_key=True, autoincrement=True, index=True)
    card_id = Column(INTEGER)
    name = Column(VARCHAR(255), index=True)
    type = Column(VARCHAR(255), index=True)
    description = Column(LONGTEXT)
    attribute = Column(VARCHAR(255), index=True)
    archtype = Column(VARCHAR(255), index=True)
    race = Column(VARCHAR(255), index=True)
    ATK = Column(VARCHAR(255), index=True)
    DEF = Column(VARCHAR(255), index=True)
    level = Column(VARCHAR(255), index=True)
    images = Column(VARCHAR(255), index=True)
    set_name = Column(VARCHAR(255), index=True)
    set_code = Column(VARCHAR(200), index=True)
    set_rarity = Column(VARCHAR(200), index=True)
    BUY_CAD_PRICE_75 = Column(VARCHAR(255), index=True)
    BUY_USD_PRICE_75 = Column(VARCHAR(255), index=True)
    AVG_CAD_PRICE = Column(FLOAT, index=True)
    AVG_USD_PRICE = Column(FLOAT, index=True)
    EBAYCA_CAD_PRICE = Column(FLOAT, index=True)
    EBAYCA_USD_PRICE = Column(FLOAT, index=True)
    EBAYCOM_CAD_PRICE = Column(FLOAT, index=True)
    EBAYCOM_USD_PRICE = Column(FLOAT, index=True)
    TCGPLAYER_CAD_PRICE = Column(FLOAT, index=True)
    TCGPLAYER_USD_PRICE = Column(FLOAT, index=True)
    YGOLEGACY_INVENTORY = Column(INTEGER, index=True)
    edition = Column(VARCHAR(200), index=True)
    condition = Column(VARCHAR(200), index=True)
    edition_id = Column(VARCHAR(200), index=True)
    cond_edition_id = Column(VARCHAR(200), index=True)
    buylist = Column(INTEGER, index=True)
    ebayca_url = Column(VARCHAR(255), default=None)
    ebaycom_url = Column(VARCHAR(255), default=None)
    tcg_url = Column(VARCHAR(255), default=None)
    ebayca_results = Column(INTEGER, default=None)
    ebaycom_results = Column(INTEGER, default=None)
    tcg_results = Column(INTEGER, default=None)
    tcg_market_price = Column(FLOAT, default=None)
    last_updated = Column(DateTime, default=None)
    ebayca_avg_cad_low = Column(FLOAT, index=True, default=None)
    ebayca_avg_cad_high = Column(FLOAT, index=True, default=None)
    ebayca_avg_usd_low = Column(FLOAT, index=True, default=None)
    ebayca_avg_usd_high = Column(FLOAT, index=True, default=None)
    ebaycom_avg_cad_low = Column(FLOAT, index=True, default=None)
    ebaycom_avg_cad_high = Column(FLOAT, index=True, default=None)
    ebaycom_avg_usd_low = Column(FLOAT, index=True, default=None)
    ebaycom_avg_usd_high = Column(FLOAT, index=True, default=None)
    tcg_avg_cad_low = Column(FLOAT, index=True, default=None)
    tcg_avg_cad_high = Column(FLOAT, index=True, default=None)
    tcg_avg_usd_low = Column(FLOAT, index=True, default=None)
    tcg_avg_usd_high = Column(FLOAT, index=True, default=None)

    tnt_cad_price = Column(FLOAT, index=True, default=None)
    tnt_usd_price = Column(FLOAT, index=True, default=None)
    tnt_results = Column(INTEGER, default=None)
    tnt_url = Column(VARCHAR(255), default=None)
    tnt_avg_cad_low = Column(FLOAT, index=True, default=None)
    tnt_avg_cad_high = Column(FLOAT, index=True, default=None)
    tnt_avg_usd_low = Column(FLOAT, index=True, default=None)
    tnt_avg_usd_high = Column(FLOAT, index=True, default=None)

    ftfg_cad_price = Column(FLOAT, index=True, default=None)
    ftfg_usd_price = Column(FLOAT, index=True, default=None)
    ftfg_results = Column(INTEGER, default=None)
    ftfg_url = Column(VARCHAR(255), default=None)
    ftfg_avg_cad_low = Column(FLOAT, index=True, default=None)
    ftfg_avg_cad_high = Column(FLOAT, index=True, default=None)
    ftfg_avg_usd_low = Column(FLOAT, index=True, default=None)
    ftfg_avg_usd_high = Column(FLOAT, index=True, default=None)

    tcg_market_price_cad = Column(FLOAT, default=None, index=True)
    history = relationship("CardPriceHistory", order_by=CardPriceHistory.id, back_populates="parent_card")
    buylist_price = Column(VARCHAR(255), index=True)

    ebay_ca_aff_urls = Column(LONGTEXT)
    ebay_com_aff_urls = Column(LONGTEXT)

